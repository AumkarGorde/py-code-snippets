# int
xint = 5
print(f'int : {xint}')

# float 
xfloat = 4.5
print(f'float : {xfloat}')

# complex
xcomplex = 3 + 4j
print(f'complex : {xcomplex}')

# bool
xbool = True
print(f'bool : {xbool}')

# str
xstr = "Hello, World!"
print(f'str : {xstr}')

# list
xlist = [1, 2, 3, 4, 5]
print(f'list : {xlist}')

# tuple
xtuple = (1, 2, 3, 4, 5)
print(f'tuple : {xtuple}')

# set
xset = {1, 2, 3, 4, 5}
print(f'set : {xset}')

# dict
xdict = {"name": "Alice", "age": 30}
print(f'dict : {xdict}')

# NoneType
xnone = None
print(f'NoneType : {xnone}')

# bytes
xbytes = b'Hello'
print(f'bytes : {xbytes}')

# bytearray
xbytearray = bytearray(5)
print(f'bytearray : {xbytearray}')

# memoryview
xmemoryview = memoryview(b'Hello')
print(f'memoryview : {xmemoryview.tolist()}')

# range
xrange = range(5)
print(f'range : {list(xrange)}')

# frozenset
xfrozenset = frozenset([1, 2, 3, 4, 5])
print(f'frozenset : {xfrozenset}')

